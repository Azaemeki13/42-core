#include "libft.h"

int	ft_count_digits(int n)
{
	int	i;

	i = 0;
	if (n == INT_MIN)
		return (10);
	if (n == 0)
		return (1);
	if (n < 0)
		n = -n;
	while (n > 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}

